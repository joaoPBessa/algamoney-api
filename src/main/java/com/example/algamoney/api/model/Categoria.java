package com.example.algamoney.api.model;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;

@Entity
@Table(name = "CATEGORIA")
@SequenceGenerator(name = Categoria.CATEGORIA_GENERATOR, allocationSize = 1, initialValue = 1, sequenceName = "CATEGORIA_CODIGO")
public class Categoria {
	
	public static final String CATEGORIA_GENERATOR = "CATEGORIA_CODIGO";
	
	@Id
	@GeneratedValue(strategy = GenerationType.SEQUENCE, generator = CATEGORIA_GENERATOR)
	@Column(name = "CODIGO")
	private Long codigo;
	
	@NotNull
	@Size(min=3, max=20)
	@Column(name = "NOME")
	private String nome;

	public Long getCodigo() {
		return codigo;
	}

	public void setCodigo(Long codigo) {
		this.codigo = codigo;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((codigo == null) ? 0 : codigo.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Categoria other = (Categoria) obj;
		if (codigo == null) {
			if (other.codigo != null)
				return false;
		} else if (!codigo.equals(other.codigo))
			return false;
		return true;
	}
	
}
